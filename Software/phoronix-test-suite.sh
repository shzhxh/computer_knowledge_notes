#### 安装

```
wget 
sudo dpkg -i *.deb
```

#### 作用

性能测试工具

#### 语法

```
phoronix-test-suite [options]
phoronix-test-suite benchmark [test | suite]
```

#### 选项

```
# 系统
# 测试的安装
# 测试
benchmark		# 安装并运行指定的测试。
# 基准测试
# OPENBENCHMARKING.ORG
# 信息
list-all-tests	# 列出OpenBenchmarking.org上所有可用的测试文件
# 创建ASSET
# 结果管理
show-result		# 显示测试结果
# 其它
# 模块
# 用户配置
# 网络与图形支持
```

